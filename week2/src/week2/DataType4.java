package week2;

public class DataType4 {
	public static void main(String[] args) {
		
		byte bData = 127;			//byte = -128~127
		char cData = 90;			//char는 음수가 없음
		float fData = 3.14f;		//숫자 뒤에 f가 붙어야함
		long iData = 1000000000L;	//정수형태는 기분이 int로 인식
		boolean boolD = true;		//true or false
		
		System.out.println("dData ="+ bData);
		System.out.println("cData ="+ cData);
		System.out.println("fData ="+ fData);
		System.out.println("iData ="+ iData);
		System.out.println("boolDa ="+ boolD);
		
	}
}
